def translate_pdf(input_path: str, output_path: str) -> dict:
    """
    Переводит PDF-файл, заменяя китайский текст на русский.
    Сохраняет изображения.
    """
    doc = fitz.open(input_path)
    stats = {"pages": len(doc), "text_blocks_translated": 0, "images_preserved": 0}
    
    for page_num in range(len(doc)):
        page = doc[page_num]
        
        # Подсчёт изображений
        images = page.get_images()
        stats["images_preserved"] += len(images)
        
        # Получаем текстовые блоки
        blocks = page.get_text("dict")["blocks"]
        
        for block in blocks:
            if "lines" not in block:
                continue
            for line in block["lines"]:
                for span in line["spans"]:
                    original_text = span["text"]
                    translated = translate_text(original_text)
                    
                    if translated != original_text:
                        stats["text_blocks_translated"] += 1
                        
                        # Получаем позицию и стиль текста
                        rect = fitz.Rect(span["bbox"])
                        font_size = span["size"]
                        
                        # Закрашиваем оригинальный текст
                        page.draw_rect(rect, color=(1, 1, 1), fill=(1, 1, 1))
                        
                        # Вставляем переведённый текст
                        try:
                            page.insert_text(
                                rect.tl,
                                translated,
                                fontsize=font_size * 0.85,  # Немного меньше для кириллицы
                                fontname="helv",
                                color=(0, 0, 0)
                            )
                        except:
                            # Если не помещается, используем меньший шрифт
                            try:
                                page.insert_text(
                                    rect.tl,
                                    translated,
                                    fontsize=font_size * 0.7,
                                    fontname="helv",
                                    color=(0, 0, 0)
                                )
                            except:
                                pass
    
    doc.save(output_path)
    doc.close()
    return stats


