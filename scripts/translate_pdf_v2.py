def extract_images_from_pdf(input_pdf: str, output_dir: str) -> list:
    """Извлекает все изображения из PDF"""
    doc = fitz.open(input_pdf)
    images = []
    
    os.makedirs(output_dir, exist_ok=True)
    
    for page_num in range(len(doc)):
        page = doc[page_num]
        image_list = page.get_images()
        
        for img_idx, img in enumerate(image_list):
            xref = img[0]
            try:
                base_image = doc.extract_image(xref)
                image_bytes = base_image["image"]
                image_ext = base_image["ext"]
                
                image_filename = f"page{page_num+1}_img{img_idx+1}.{image_ext}"
                image_path = os.path.join(output_dir, image_filename)
                
                with open(image_path, "wb") as f:
                    f.write(image_bytes)
                
                # Получаем позицию изображения на странице
                for item in page.get_image_info():
                    if item["xref"] == xref:
                        images.append({
                            "page": page_num,
                            "path": image_path,
                            "bbox": item["bbox"],
                            "width": item["width"],
                            "height": item["height"]
                        })
                        break
            except Exception as e:
                print(f"Ошибка извлечения изображения: {e}")
    
    doc.close()
    return images


