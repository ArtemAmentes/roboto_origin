def translate_text(text: str) -> str:
    """Переводит текст"""
    result = text
    for cn, ru in sorted(TRANSLATIONS.items(), key=lambda x: -len(x[0])):
        result = result.replace(cn, ru)
    return result


def translate_pdf_preserve_images(input_path: str, output_path: str) -> dict:
    """
    Переводит PDF, сохраняя все изображения.
    Использует insert_textbox вместо redaction для кириллицы.
    """
    doc = fitz.open(input_path)
    stats = {"pages": doc.page_count, "replacements": 0, "images": 0}
    
    # Регистрируем шрифт
    font = fitz.Font(fontfile=FONT_PATH)
    
    for page_num in range(doc.page_count):
        page = doc[page_num]
        
        # Считаем изображения
        stats["images"] += len(page.get_images())
        
        # Собираем текстовые блоки с китайским
        chinese_spans = []
        blocks = page.get_text("dict")["blocks"]
        
        for block in blocks:
            if "lines" not in block:
                continue
            for line in block["lines"]:
                for span in line["spans"]:
                    text = span["text"]
                    if re.search(r'[\u4e00-\u9fff]', text):
                        translated = translate_text(text)
                        chinese_spans.append({
                            "rect": fitz.Rect(span["bbox"]),
                            "translated": translated,
                            "size": span["size"],
                            "color": span.get("color", 0)
                        })
        
        # Закрашиваем китайский текст белым и вставляем русский
        for span_info in chinese_spans:
            rect = span_info["rect"]
            
            # Рисуем белый прямоугольник поверх китайского текста
            page.draw_rect(rect, color=(1, 1, 1), fill=(1, 1, 1))
            
            # Вставляем русский текст с помощью insert_textbox
            font_size = span_info["size"] * 0.75
            
            # Расширяем rect для текста
            text_rect = fitz.Rect(
                rect.x0,
                rect.y0,
                rect.x1 + 50,  # Даём больше места для русского текста
                rect.y1 + 5
            )
            
            page.insert_textbox(
                text_rect,
                span_info["translated"],
                fontname="F0",
                fontfile=FONT_PATH,
                fontsize=font_size,
                color=(0, 0, 0),
                align=fitz.TEXT_ALIGN_LEFT
            )
            
            stats["replacements"] += 1
    
    doc.save(output_path)
    doc.close()
    return stats


